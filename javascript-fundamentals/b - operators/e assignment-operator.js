// var1 += 10; takes the value of var 1 and adds 10 to it
// var1 -= 10; takes the value of var 1 and subtracts 10 from it
// var1 /= 10; takes the value of var 1 and divides it by 10
// var1 *= 10; takes the value of var 1 and multiplies it by 10
// var1 %= 10; takes the value of var 1 and returns the remainder after dividing by 10
